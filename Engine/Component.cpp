#include "pch.h"
#include "Component.h"
#include "Entity.h"

/******************************************************************************
******************************************************************************/
Component::Component()
    : m_owner(nullptr)
    , m_isActive(true)
{};

/******************************************************************************
******************************************************************************/
Component::Component(Entity* owner)
    : m_owner(owner)
    , m_isActive(true)
{};

/******************************************************************************
******************************************************************************/
void Component::SetActive(bool active)
{
	m_isActive = active;
}

/******************************************************************************
******************************************************************************/
bool Component::isActive()
{
    return m_isActive;
}
